QUESTIONS_COUNT = 20


@app.route("/")
def start():
    return render_template("start.html")


@app.route("/begin")
def begin():
    selected_words = random.sample(words, QUESTIONS_COUNT)

    session["quiz_words"] = selected_words
    session["current"] = 0
    session["score"] = 0

    return redirect(url_for("question"))


@app.route("/question", methods=["GET", "POST"])
def question():
    quiz_words = session.get("quiz_words")

    if not quiz_words:
        return redirect(url_for("start"))

    if request.method == "POST":
        selected = request.form.get("answer")
        correct = request.form.get("correct")

        if selected == correct:
            session["score"] += 1

        session["current"] += 1

    current = session["current"]

    if current >= QUESTIONS_COUNT:
        return redirect(url_for("result"))

    word_data = quiz_words[current]

    return render_template(
        "index.html",
        word_data=word_data,
        current=current + 1,
        total=QUESTIONS_COUNT,
        score=session["score"]
    )


@app.route("/result")
def result():
    score = session.get("score", 0)

    if 5 <= score < 10:
        message = "Неплохо, но можно и лучше"
    elif 10 <= score < 15:
        message = "Довольно хорошо, ты почти готов(а)"
    elif 15 <= score <= 20:
        message = "Молодец, ты справился со всеми вариантами"
    else:
        message = "Стоит ещё потренироваться"

    return render_template(
        "result.html",
        score=score,
        total=QUESTIONS_COUNT,
        message=message
    )


@app.route("/restart")
def restart():
    session.clear()
    return redirect(url_for("start"))


